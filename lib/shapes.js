class Shapes {
    constructor(text, textColor, shapeColor) {
        this.text = text
        this.textColor = textColor
        this.shapeColor = shapeColor
    }
}

class Circle extends Shapes {
    constructor(text,textColor,shapeColor)
}

class Triangle extends Shapes {
    constructor(text,textColor,shapeColor)
}

class Square extends Shapes {
    constructor(text, textColor, shapeColor)
}